public class FacePrinter {

    public static void main(String[] args) {

        // This section is the face's hairline and forehead
        System.out.println("        \\\\\\\\\\\\///////");
        System.out.println("        |           |");

        //This section is the face's eyes/glasses
        System.out.println("       /---\\____/---\\");
        System.out.println("       | O |/  \\| O | ");
        System.out.println("       \\---/    \\---/");

        //This section is the face's nose area
        System.out.println("      /   { ~  ~ }   \\");

        //This section is the mouth and chin area of the face
        System.out.println("     |  _         _   |");
        System.out.println("     \\   \\_______/    /");
        System.out.println("      \\              /");
        System.out.println("       \\___(_^^_)___/");

    }
}
